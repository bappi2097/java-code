    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         databaseConnection con = new databaseConnection();
        Connection c= con.connect();

        String name,email,NId,phone,address,userName,password,Retypepass;
        name=nameField.getText();
        email=emailField.getText();
        NId=nidField.getText();
        phone=phoneField.getText();
        address=addressField.getText();
        userName=usernameField.getText();
        password=passwordField.getText();
        Retypepass=repassField.getText();
        String sql="insert into studentRegistration(Name, Email, NID, Phone, Address, UserName, Password, ReTypePassword) values(?, ?, ?, ?, ?, ?, ?, ?);";
        try{
            try (PreparedStatement pst = c.prepareStatement(sql)) {
                pst.setString(1, name);
                pst.setString(2, email);
                pst.setString(3, NId);
                pst.setString(4, phone);
                pst.setString(5, address);
                pst.setString(6, userName);
                pst.setString(7, password);
                pst.setString(8, Retypepass);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registration Succesfull");
            }
            c.close();
        }catch(HeadlessException | SQLException e){
            System.err.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

