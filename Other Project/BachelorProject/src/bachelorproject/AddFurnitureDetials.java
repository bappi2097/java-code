    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        databaseConnection con = new databaseConnection();
        Connection c= con.connect();

        String furnitureNo ,furnitureName,furniturePrice;
        furnitureNo=furnitureNoField.getText();
        furnitureName=furnitureNameField.getText();
        furniturePrice=furniturePriceField.getText();
        
        String sql="insert into FurnitureDetials(FurnitureNo, FurnitureName, FurniturePrice) values(?, ?, ?);";
        try{
            try (PreparedStatement pst = c.prepareStatement(sql)) {
                pst.setString(1, furnitureNo);
                pst.setString(2, furnitureName);
                pst.setString(3, furniturePrice);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Add Succesfull");
            }
            c.close();
        }catch(HeadlessException | SQLException e){
            System.err.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

