    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        SQLiteDBConnection con = new SQLiteDBConnection();
        Connection c = SQLiteDBConnection.connect();
        ID=txtStudentID.getText();
        Name = txtStudentName.getText();
        Number = txtNumber.getText();
        Email = txtEmail.getText();
        Address = txtAddress.getText();
        if(rdbtnMale.isSelected())
        Gender="Male";
        else if(rdbtnFemale.isSelected())
        Gender="Female";
        else
        Gender="Not Selected";
        Age = cmbDD.getSelectedItem().toString()+"/"+cmbMM.getSelectedItem()
        .toString()+"/"+cmbYYYY.getSelectedItem().toString();
            String sql = "INSERT INTO StudentInfo (ID,Name,Email,Address,Gender,"
                    +"BirthDate,PhoneNumber) VALUES (?,?,?,?,?,?,?);"; 
            try {
                try (PreparedStatement pst = c.prepareStatement(sql)) {
                    pst.setString(1, ID);
                    pst.setString(2, Name);
                    pst.setString(3, Email);
                    pst.setString(4, Address);
                    pst.setString(5, Gender);
                    pst.setString(6, Age);
                    pst.setString(7, Number);
                    pst.execute();
                    System.out.println("save successfull");
                }
                c.close();
            } catch (Exception e) {
                System.err.println(e.getClass().getName() + ": " + e.getMessage());
            }
            System.out.println("Records created successfully");
    }//GEN-LAST:event_btnSaveActionPerformed

